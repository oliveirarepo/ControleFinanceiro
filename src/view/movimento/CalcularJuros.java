    private void Calcular_JurosComposto()
    {
        DecimalFormat DF = new DecimalFormat("##0.00");
        double capital = Double.parseDouble(TACapital.getText().replaceAll(",","."));
        int tempo = Integer.parseInt(TATempo.getText());
        double JurosMensal = Double.parseDouble(TAJuros.getText().replaceAll(",", ".") ) / 100;
        double Total = 0;
         JurosMensal = 1 + JurosMensal;
         double Multiplicador = JurosMensal;
       
        for( int i = 2;i<=tempo; i++)
        {
            JurosMensal = JurosMensal * Multiplicador;
            
        }

        Total = capital * JurosMensal;
        TATotal.setText(DF.format(Total));
        TATotalJuros.setText(DF.format( Total - capital));

        //M = ? (Ã© o valor que queremos saber)
        //C = R$ 4000,00
        //i = 4% /100 = 0,04
        //t = 5
//        M = 4000 * (1 + 0,04)5
//        M= 4000 * (1,04)5
//        M= 4000 * 1,2165
//        M= 4866

    }
    private void JurosSimples()
    {
        DecimalFormat DF = new DecimalFormat("##0.00");
        double capital = Double.parseDouble(TACapital.getText().replaceAll(",","."));
        double SomaJuros = capital;
        int tempo = Integer.parseInt(TATempo.getText());
        double JurosMensal = Double.parseDouble(TAJuros.getText().replaceAll(",", ".") ) / 100;
        double Total = 0;
        Total = capital * JurosMensal * tempo;
        TATotal.setText(DF.format(Total + capital));
        TATotalJuros.setText(DF.format(Total - SomaJuros));
    }

